/**
 * Generic method.
 *
 * @internal
 * @returns - The product of x and y.
 */
export function methodA(): number {
  const x = 1;
  const y = 2;

  return x * y;
}

/**
 * Generic method.
 *
 * @beta
 * @param x - The initial value of x.
 * @param y - The initial value of y.
 * @returns - The product of x and y.
 */
export function methodB(x: number, y: number): number {
  return x * y;
}
